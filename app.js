function showPage(pageId) {
    console.log('LearnQuest SIH: Showing page:', pageId);
    
    // Update active nav link
    navLinks.forEach(link => {
        link.classList.remove('active');
        if (link.getAttribute('data-page') === pageId) {
            link.classList.add('active');
        }
    });

    // Update active page
    pages.forEach(page => {
        page.classList.remove('active');
    });

    const targetPage = document.getElementById(pageId);
    if (targetPage) {
        targetPage.classList.add('active');
        appState.currentPage = pageId;
        console.log('LearnQuest SIH: Page activated:', pageId);
        
        // Close sidebar on mobile
        if (sidebar) sidebar.classList.remove('open');
        
        // Page-specific initialization
        switch(pageId) {
            case 'dashboard':
                renderDashboard();
                break;
            case 'subjects':
                renderSubjects();
                break;
            case 'progress':
                renderProgressCharts();
                break;
            case 'gamification':
                renderGamification();
                break;
            case 'chatbot':
                setupChatbot();
                break;
            case 'settings':
                renderSettings();
                break;
            case 'onboarding':
                initializeOnboarding();
                break;
            case 'impact':
                renderImpact();
                break;
        }
    } else {
        console.warn('LearnQuest SIH: Page not found:', pageId);
    }
}

function switchLanguage(langCode) {
    console.log('LearnQuest SIH: Switching language to:', langCode);
    appState.currentLanguage = langCode;
    
    // Update language buttons
    document.querySelectorAll('.language-btn').forEach(btn => {
        btn.classList.remove('active');
        if (btn.getAttribute('data-lang') === langCode) {
            btn.classList.add('active');
        }
    });
    
    applyLanguage(langCode);
    
    // Re-render current page content with new language
    switch(appState.currentPage) {
        case 'dashboard':
            renderDashboard();
            break;
        case 'subjects':
            renderSubjects();
            break;
        case 'chatbot':
            setupChatbot();
            break;
        case 'gamification':
            renderGamification();
            break;
    }
    
    console.log('LearnQuest SIH: Language switched to:', langCode);
}

function applyLanguage(langCode) {
    const t = translations[langCode] || translations['en'];
    
    document.querySelectorAll('[data-translate]').forEach(element => {
        const key = element.getAttribute('data-translate');
        if (t[key]) {
            if (element.tagName === 'INPUT' && element.type === 'button') {
                element.value = t[key];
            } else {
                element.textContent = t[key];
            }
        }
    });
    
    document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
        const key = element.getAttribute('data-translate-placeholder');
        if (t[key]) {
            element.placeholder = t[key];
        }
    });
}

