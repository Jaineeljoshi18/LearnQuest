function showAchievementModal(icon, title, description, reward) {
    const modal = document.getElementById('achievementModal');
    const iconEl = document.getElementById('achievementIcon');
    const titleEl = document.getElementById('achievementTitle');
    const descEl = document.getElementById('achievementDescription');
    const rewardEl = document.getElementById('pointsEarned');
    
    if (modal && iconEl && titleEl && descEl) {
        iconEl.textContent = icon;
        titleEl.textContent = title;
        descEl.textContent = description;
        if (rewardEl && reward) {
            rewardEl.textContent = reward;
        }
        
        modal.classList.remove('hidden');
        
        const closeBtn = document.getElementById('closeAchievement');
        if (closeBtn) {
            closeBtn.onclick = function() {
                modal.classList.add('hidden');
            };
        }
    }
}

